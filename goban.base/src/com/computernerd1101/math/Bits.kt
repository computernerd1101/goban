fun countTrailingZeroBits(x: Byte): Int {
    return ntz8tab[x.toInt() and 0xFF].toInt()
}

fun countTrailingZeroBits(x: Short): Int {
    return if (x.toInt() == 0) 16
    else deBruijn32tab[(x.toInt() and -x.toInt()) * deBruijn32 ushr (32 - 5)].toInt()
}

fun countTrailingZeroBits(x: Int): Int {
    return if (x == 0) 32
    else deBruijn32tab[(x and -x) * deBruijn32 ushr (32 - 5)].toInt()
}

fun countTrailingZeroBits(x: Long): Int {
    return if (x == 0L) 64
    else deBruijn64tab[((x and -x)*deBruijn64 ushr (64 - 6)).toInt()].toInt()
}